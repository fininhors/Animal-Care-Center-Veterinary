<h3 class="title">World Rabies Day</h3>
<p class="date">September 28, 2013</p>
<div class="article">
  <div class="event-text">
  	<p>Celebrate World Rabies Day at Animal Care Center veterinary clinic in Winchester. World Rabies Day is designed to raise awareness about both human and animal rabies.  The day is also about teaching everyone about the impact of rabies, how to prevent it and how to eradicate sources of the disease across the world.</p>
    <p>Animal Care Center will be offering discounted rabies vaccines for dogs that are due, and will give out free items to people who come with their pets that have already been vaccinated for rabies. There will also be a  Rummage Sale to benefit Animal Harbor.</p>
    <p>The event will be from 8 AM to 1 PM at the Animal Care Center veterinary clinic.</p>
