          <p>All efforts are taken to ensure that your pet is healthy before anesthesia and surgery is performed. Pre-anesthetic bloodwork can be performed to better ensure that your pet will be able to process the anesthesia normally, and there are no underlying diseases that would affect your pet's health. Spaying and neutering can cause minor discomfort for your pet. Medication is used during surgery and is available to use at home to lessen any discomfort s/he will encounter.</p>
