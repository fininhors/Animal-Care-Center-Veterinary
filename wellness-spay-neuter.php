          <h1>Why Should I Spay or Neuter My Pet?</h1>
          <p>ALL pets not intended for planned breeding programs should be surgically spayed or neutered by your veterinarian. In addition to improved health and behavior resulting from these procedures, you will be doing your part to prevent further unwanted puppies and kittens. Millions of unwanted animals are euthanized each year because there are simply not enough good homes available for all the adoptable pets.<p>
          <h2>What are the Benefits of Spaying or Neutering My Pet?</h2>
          <h3>Females</h3>
          <ul>
          <li>No unplanned or unwanted pregnancy</li>
            <li>Prevents signs of heat cycles &ndash; dogs have periodic bleeding during heat cycles (every 6 months)</li>
            <li>Decreases the incidence of mammary/breast cancer by 80% if spay performed before the 1st heat cycle</li>
            <li>Eliminates possibility of cystic ovaries</li>
            <li>Eliminates possibility of uterine infection (life threatening!)</li>
          </ul>
          <h3>Males</h3>
          <ul>
            <li>No unplanned or unwanted pregnancy</li>
            <li>Decreases desire to roam</li>
            <li>Decreases the incidence of prostate disease, and the possibility of testicular diseases are eliminated</li>
            <li>Decreases odor of tomcat urine</li>
            <li>Can prevent spraying in male cats and marking in male dogs</li>
            <li>Prevents undesirable sexual behavior</li>
            <li>Decreases aggression</li>
            <li>Decreases formation of testosterone-sensitive tumors and hernias</li>
          </ul>
