    <p>The Franklin County Humane Society invites everyone to come to their Spring 2012 Party for Paws benefit for their shelter, Animal Harbor, set for Saturday, April 28 from 6-8:30 p.m. at Arezzo's in Winchester. </p>
    <p>Enjoy appetizers and drinks and a silent auction with a variety of items to bid on. The proceeds   from this event will be used by Animal Harbor to rescue, care for and find loving homes for companion animals abandoned in Franklin County.</p>
