<h3 class="title">Sale To Benefit Animal Harbor</h3>
<div class="article">
 <p class="date">April 08, 2011</p>
  <p class="publication">The Herald-Chronicle</p>
  <div class="article-text">
   <p>On Saturday, April 16, Belk stores throughout the South will hold a special 4-hour charity sale (6-10 am). Admission is by ticket only, and tickets are only $5. If you buy a ticket from The Franklin County Humane Society, all proceeds will go to care for the homeless pets Animal Harbor. Tickets can be used in any Belk store. This is a great chance to find the latest fashions or prepare your home for spring. During this event, Belk offers:</p>
    <ul>
     <li>discounts from 20-70 percent on purchases throughout the store, including some rarely discounted brands.</li>
     <li>$5 off first purchase of $5 or more at the event</li>
     <li>First 100 customers at each store will receive free Belk gift cards ranging in value from $5 to $1,000.</li>
     <li>all participating charities will be automatically registered to win one of three $1,000 donations from Belk in a company-wide drawing.</li>
    </ul>
    <p>The event, held semi-annually since 2007, has raised over $18 million for participating charities throughout the Southeast.</p>
    <p>For tickets, please contact Animal Harbor at 962-4472, Animal Care Center at 967-6345, Jennifer Michael at jmichael@sewanee.edu, Pat Thompson at pthompson313@gmail.com or Susan Rupert at srupert@sewanee.edu. </p>
