<h3 class="title">Franklin County Rabies Clinic Schedule Announced </h3>
<div class="article">
 <p class="date">March 15, 2011</p>
  <p class="publication">The Herald-Chronicle</p>
  <div class="article-text">
   <p>The Franklin County Animal Control has announced the dates for the annual Franklin County Rabies Clinic.</p>
   <p>Because a licensed veterinarian must administer rabies vaccinations, Dr. Harry Prince and staff with Town and Country Veterinary Hospital, as well as Dr. Traci Helton and staff with Mobile Veterinary Services will be conducting the clinic. All local veterinarian offices including Animal Care Center also work together during the month of March to offer the rabies vaccination at their respective offices with no appointment necessary.</p>
   <p>Animal Control Shelter Manager Jennifer Housley reminds residents of Franklin County that state law requires all dogs that are three-months-old and cats four-months-old and older must have current vaccinations.</p>
   <p>&quot;For three straight Saturdays in a row rabies clinics will be set up at community centers, churches and parks across the county,&quot; Housley said. &quot;All pet owners are asked to come take advantage of the Franklin County Rabies Clinic. The cost is only $9 per dog or cat.&quot;</p>
   <p>The following are the dates, time and place of each rabies clinic:    </p>
   <p><strong>Saturday, March 19</strong></p>
   <ul>
    <li>Cowan from 8-9a.m. at Police Dept (Old City Hall) </li>
    <li>Sewanee from 9:15-10:15 a.m. at Public School </li>
    <li>Midway from 10:30-11:30 a.m. at Midway Store </li>
    <li>Winchester Springs 1:15-2:15 p.m. at Church of Jesus Christ (Old School) </li>
    <li>Center Grove 2:30-3:45 p.m. at Community Center</li>
   </ul>
	<p><strong>Saturday, March 26</strong></p>
   <ul>
    <li>Broadview from 8-9:30 a.m. at Bethel Methodist Church </li>
    <li>Lexie from 10:00-10:45 a.m. at Lexie Church </li>
    <li>Belvidere from 11:10-11:55 a.m. at Community Center </li>
    <li>Old Salem from 1:15-1:45 p.m. at Salem House </li>
    <li>Huntland from 2-3 p.m. at City Hall </li>
    <li>Centennial from 3:30-4:00 p.m. at Centennial Grocery </li>
    <li>Estill Springs from 1-2:15 p.m. at City Park </li>
    <li>Capitol Hill from 2:30-3:00 p.m. at Community Center </li>
    <li>Oak Grove from 3:20-3:45 p.m. at Community Center </li>
    <li>Decherd from 4:00-4:15 p.m. at Old City Hall</li>
   </ul>
	<p><strong>Saturday, April 2</strong></p>
   <ul>
    <li>Winchester from 9-11 a.m. at Board of Education Special Services (Old Health Dept)</li>
   </ul>
	<p>Residents are invited to take their dogs and cats to be vaccinated at the clinic most convenient from them. Anyone with questions about the Franklin County Rabies Clinic can call 967-5389. </p>
