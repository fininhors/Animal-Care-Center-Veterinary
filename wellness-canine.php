					<h2>Canine Vaccinations </h2>
					<ul>
          	<li><strong>Distemper, Canine hepatitis, Parvovirus, Parainfluenza, Coronavirus</strong> &ndash; these viruses can be life-threatening for your pet. Vaccination is recommended yearly for all dogs.</li>
            <li><strong>Leptospirosis</strong> &ndash; Leptosprosis is a bacterial disease that can be carried by raccoons and spread in stagnant water in rural and urban areas. This disease can be contracted by people from the environment or urine of infected animals. This vaccine is recommended yearly for most dogs.</li>
            <li><strong>Rabies</strong> &ndash; Due to Tennessee being an endemic area for rabies virus and the potential spread of this fatal disease to humans, the state continues to require rabies vaccines to be given to all dogs. The a 1-year rabies vaccine and a 3-year rabies vaccine is available for dogs. Both vaccines are considered legal in the following counties; Bedford, Lincoln, Franklin, Moore, Grundy, and Coffee counties.</li>
            <li><strong>Bordetella</strong> &ndash; Bordetella is a respiratory pathogen that can be easily aerosolized and transmitted between dogs. The vaccination for this disease is required by many boarding kennels (including Animal Care Center) and grooming facilities. We recommend this vaccine for any dogs that come into contact with dogs outside of their household.</li>
            <li><strong>Lyme's Disease</strong> &ndash; This is a bacterial disease that is carried by ticks. Combining this vaccination with tick prevention is recommended for hunting dogs, outside dogs, and any dog that is likely to come in contact with ticks. If your dog has not had a Lyme's vaccine before, he will need a series of two vaccinations one month apart. <em>Note: This vaccine is not generally recommended to our clients unless the dog will be traveling to areas where Lyme disease is endemic, for example above the Mason-Dixon line.</em></li>
        	</ul>
	        <h2>Heartworm Disease</h2>
        	<ul>
            <li><strong>Heartworm Antigen Test</strong> &ndash; a yearly test to ensure your dog has not been infected by heartworms. If your dog is over 6 months old and has not been on heartworm prevention consistently, it is required to have a heartworm test before starting prevention.</li>
            <li><strong>Heartworm Prevention</strong> &ndash; a monthly chewable or topical preventive should be given to all dogs to prevent life-threatening heartworm disease. Intestinal parasites are then also controlled with these medications.</li>
        	</ul>
        	<h2>Intestinal Parasites</h2>
        	<ul>
            <li><strong>De-wormer for puppies</strong> &ndash; we always recommend de-worming all puppies for hookworms and roundworms every two weeks for four doses.</li>
            <li><strong>Fecal exam</strong> &ndash; all puppies should have an initial stool sample checked for parasites. Adult dogs should have a yearly fecal exam performed. Dogs and puppies can carry tapeworms, coccidia, and whipworms that are not treated with our general de-wormer.</li>
					</ul>
